package sample.api.facebook;

public class Profile {

	private String id;
	private String name;

	public Profile() {
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setId(String id) {
		this.id = id;
	}
}
